jour = input("Entrez un jour sous le format nombre (1-7) : ")

if(jour == 1):
    print("Lundi")
elif(jour == 2):
    print("Mardi")
elif(jour == 3):
    print("Mercredi")
elif(jour == 4):
    print("Jeudi")
elif(jour == 5):
    print("Vendredi")
elif(jour == 6):
    print("Samedi")
else:
    print("Dimanche")
        

